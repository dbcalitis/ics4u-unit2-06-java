/**
 * Airplane Class.
 */
public class Airplane {
    /**
     * Speed variable.
     */
    private int speed;

    /**
     * The getSpeed function.
     *
     * <p>
     * returns the speed.
     * </p>
     *
     * @return speed - the speed of the airplane.
     */
    public int getSpeed() {
        return this.speed;
    }

    /**
     * The setSpeed function.
     *
     * <p>
     * Sets the speed.
     * </p>
     *
     * @param speed - the speed of the airplane.
     */
    public void setSpeed(int speed) {
        this.speed = speed;
    }
}
